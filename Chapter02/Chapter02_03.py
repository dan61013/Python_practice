"""
位元運算子
"""

def main():
    
    # 位元運算
    # 範例為二進位
    # 5 = 101, 0101
    # 4 = 100, 0100
    a = 5&4 # 0100
    print(a)
    
    a = 5|4 # 0101 (二進位)
    print(a)
    
    a = 5^4 # 0001
    print(a)
    
    a = ~5 # ~(0101)=1010 -> 開頭是1表示負數，a=-6
    print(a)
    
    a = 5<<2 # 0101左移2位 -> 10100, a = 20
    print(a)
    
    a = 5>>2 # 0101右移2位 -> 0001, a = 1
    print(a)

if __name__ == '__main__':
    main()