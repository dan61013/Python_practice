"""
String 字串
三引號、換行符號、合併、切割字串
"""

def main():
    
    # 三引號運用
    s3 = '''
    春眠不覺曉，處處聞啼鳥。
    夜來風雨聲，花落知多少。
    '''
    
    print(s3)
    
    # 換行符號運用
    s = '春眠不覺曉，處處聞啼鳥。\n\
夜來風雨聲，花落知多少。\n\
\t作者:孟浩然 詩名:春曉'
    print(s)
    
    # 串接/切割字串
    s1 = '01234'
    s2 = '56789'
    s3 = s1 + s2
    print(s3)
    print(s1*3)
    print(s1[0], s1[3:], s1[-1])

if __name__ == '__main__':
    main()